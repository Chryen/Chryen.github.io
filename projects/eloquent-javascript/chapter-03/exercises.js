// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
    if(num1 > num2){
        return num2;    // return num2 as min if num1 > num2
    } else if(num1 < num2){
        return num1;    // return num1 as min if num1 < num2
    } else{
        return num1;    // return num1 as min if num1 = num2
    }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
    if(num === 0){
        return true;    // if num = 0 return true
    } else if(num === 1){
        return false;   // if num = 1 return false
    } else if(num % 2 === 0){
        return true;    // if num is divided evenly by 2 (even) return true
    } else {
        return false;   // all other results return false
    }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
    var count = 0;      // initialize count to hold result
    for(let i = 0; i < string.length; i++){
        if(string[i] === char){
            count++;    // loop through each letter of string and check if it equals char
        }
    }
    return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
    var count = 0;      // initialize count to hold result
    for(let i = 0; i < string.length; i++){
        if(string[i] === "B"){
            count++;    // loop through each letter of string and check if it equals "B"
        }
    }
    return count;
}

